package com.hybrid.framework.services;

import com.hybrid.framework.api.ApiAuthManager;
import com.hybrid.framework.api.ApiUtils;
import com.hybrid.framework.context.ContextKeys;
import com.hybrid.framework.context.TestContext;
import com.hybrid.framework.utils.JsonUtils;
import io.restassured.response.Response;
import org.apache.logging.log4j.LogManager;
import org.apache.logging.log4j.Logger;
import org.testng.Assert;

import com.fasterxml.jackson.databind.node.ObjectNode;

import java.util.Map;

/**
 * Domain-agnostic HTTP orchestration for BDD steps. Uses {@link TestContext} for
 * {@code lastResponse} and the request body at {@link ContextKeys#API_PAYLOAD}.
 */
public class ApiHttpService {

    private static final Logger LOG = LogManager.getLogger(ApiHttpService.class);

    private final TestContext testContext;

    // ??????????????????????????????????????????????????????????????
    // Constructor
    // ??????????????????????????????????????????????????????????????

    // Wires HTTP orchestration to the shared test context.
    public ApiHttpService(TestContext testContext) {
        this.testContext = testContext;
    }

    // ??????????????????????????????????????????????????????????????
    // GET
    // ??????????????????????????????????????????????????????????????

    // Sends GET to the endpoint and stores the response.
    public void get(String endpoint) {
        storeResponse(ApiUtils.get(endpoint));
    }

    // Sends GET with a single path parameter.
    public void getWithPathParam(String endpoint, String paramName, String paramValue) {
        storeResponse(ApiUtils.getWithPathParams(endpoint, Map.of(paramName, paramValue)));
    }

    // Sends GET with a path parameter value taken from test context.
    public void getWithPathParamFromContext(String endpoint, String paramName, String contextKey) {
        Object value = requireContextValue(contextKey);
        getWithPathParam(endpoint, paramName, String.valueOf(value));
    }

    // ??????????????????????????????????????????????????????????????
    // POST
    // ??????????????????????????????????????????????????????????????

    // Sends POST with the body stored at {@link ContextKeys#API_PAYLOAD}.
    public void post(String endpoint) {
        storeResponse(ApiUtils.post(endpoint, requireApiPayload()));
    }

    /**
     * POST with {@link ApiAuthManager} token (e.g. RealWorld {@code Authorization: Token}).
     * Uses {@link ContextKeys#ACCESS_TOKEN} from {@link TestContext}.
     */
    public void postAuthenticated(String endpoint) {
        applyTokenFromContext();
        storeResponse(ApiUtils.post(endpoint, requireApiPayload()));
    }

    // ??????????????????????????????????????????????????????????????
    // PUT
    // ??????????????????????????????????????????????????????????????

    // Sends PUT with a path parameter and the body at {@link ContextKeys#API_PAYLOAD}.
    public void putWithPathParam(String endpoint, String paramName, String paramValue) {
        storeResponse(ApiUtils.put(
                endpoint, Map.of(paramName, paramValue), requireApiPayload()));
    }

    // Sends PUT with a path parameter value taken from test context.
    public void putWithPathParamFromContext(String endpoint, String paramName, String contextKey) {
        Object value = requireContextValue(contextKey);
        putWithPathParam(endpoint, paramName, String.valueOf(value));
    }

    // ??????????????????????????????????????????????????????????????
    // DELETE
    // ??????????????????????????????????????????????????????????????

    // Sends DELETE with a single path parameter.
    public void deleteWithPathParam(String endpoint, String paramName, String paramValue) {
        storeResponse(ApiUtils.delete(endpoint, Map.of(paramName, paramValue)));
    }

    // Sends DELETE with a path parameter value taken from test context.
    public void deleteWithPathParamFromContext(String endpoint, String paramName, String contextKey) {
        Object value = requireContextValue(contextKey);
        deleteWithPathParam(endpoint, paramName, String.valueOf(value));
    }

    // ??????????????????????????????????????????????????????????????
    // Payload configuration
    // ??????????????????????????????????????????????????????????????

    /**
     * Loads a JSON payload file from {@code src/test/resources/payloads/} into
     * {@link ContextKeys#API_PAYLOAD} for the next POST/PUT.
     *
     * @param fileName payload file name only (e.g. {@code register_user.json})
     */
    public void setPayloadFromFile(String fileName) {
        setPayloadFromFile(fileName, null);
    }

    /**
     * Loads a JSON template once and applies all field overrides in a single pass.
     * <p>
     * Override values can be mixed freely in the same map or feature-file table:
     * literals, {@link JsonUtils#OVERRIDE_MISSING}, {@link JsonUtils#OVERRIDE_EMPTY},
     * {@link JsonUtils#OVERRIDE_NULL},
     * and {@code <context:keyName>} (resolved from {@link TestContext} during mutation).
     * </p>
     *
     * @param fileName       payload file name under {@code src/test/resources/payloads/}
     * @param fieldOverrides dot-path overrides; {@code null} or empty loads the template as-is
     */
    public void setPayloadFromFile(String fileName, Map<String, String> fieldOverrides) {
        ObjectNode body;
        if (fieldOverrides == null || fieldOverrides.isEmpty()) {
            body = JsonUtils.readPayloadAsTree(fileName);
            LOG.info("Loaded payload from '{}' into TestContext", fileName);
        } else {
            body = JsonUtils.readPayloadWithOverrides(
                    fileName,
                    fieldOverrides,
                    contextKey -> testContext.get(contextKey));
            LOG.info("Loaded payload from '{}' with {} override(s) into TestContext", fileName,
                    fieldOverrides.size());
        }
        testContext.put(ContextKeys.API_PAYLOAD, body);
    }

    /** Stores a request body on {@link ContextKeys#API_PAYLOAD} for the next POST/PUT. */
    public void setPayload(Object body) {
        testContext.put(ContextKeys.API_PAYLOAD, body);
    }

    // ??????????????????????????????????????????????????????????????
    // Private helpers
    // ??????????????????????????????????????????????????????????????

    private Object requireApiPayload() {
        Object payload = testContext.get(ContextKeys.API_PAYLOAD);
        Assert.assertNotNull(payload,
                "API payload must be set (setPayloadFromFile or setPayload) before POST/PUT");
        return payload;
    }

    // Loads a value from context or fails if the key was never set.
    private Object requireContextValue(String contextKey) {
        Object value = testContext.get(contextKey);
        Assert.assertNotNull(value, "Context key '" + contextKey + "' must be set before this request");
        return value;
    }

    // Syncs TestContext access token into ApiAuthManager for ApiUtils default spec.
    private void applyTokenFromContext() {
        String token = testContext.get(ContextKeys.ACCESS_TOKEN, String.class);
        Assert.assertNotNull(token,
                "AccessToken must be stored in TestContext before authenticated API calls");
        ApiAuthManager.setAccessToken(token);
    }

    // Persists the Rest Assured response as the last API response on context.
    private void storeResponse(Response response) {
        testContext.setLastResponse(response);
    }
}
